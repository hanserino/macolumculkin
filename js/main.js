/*  ================================================================================
 DEFAULT PARAMETERS
 ================================================================================  */
var mobileWidth = 650,
	$smallClass = 'small',
	$mediumClass = 'medium',
	$largeClass = 'large';

/*  ================================================================================
 CHECKS AND STUFF
 ================================================================================  */
function onResize() {
	var windowWidth = $(window).width();
	if (windowWidth <= mobileWidth) {
		$('body').attr('data-device-size', $smallClass);
	}
	else {
		$('body').removeAttr('data-device-size', $smallClass);
	}

}




/*  ================================================================================
 DOCUMENT READY
 ================================================================================  */
$(document).ready(function() {
	onResize();
});


/*  ================================================================================
	ON RESIZE
 ================================================================================  */
$(window).resize(onResize);
